package com.infir.gop;

public class SecondPattern {

	public static void main(String[] args) {
		int i,j;
		int k=0;
		//Right angle triangle pattern example with numbers
		for(i=0;i<6;i++){
			for(j=0;j<6;j++){
				if(j<=i){
				System.out.print(" "+j);
	            
				}
			}
			System.out.println();
		}

	}

}
